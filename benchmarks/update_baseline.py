#!/usr/bin/env python3
"""Update performance baseline after merge to main.

Runs 500 episodes, measures all SLO metrics, and writes baseline.json.
Intended to be run via ``workflow_dispatch`` in CI after merge approval.

Usage:
    python benchmarks/update_baseline.py              # Interactive
    python benchmarks/update_baseline.py --confirm     # Non-interactive (CI)
"""

from __future__ import annotations

import argparse
import gc
import json
import os
import statistics
import subprocess
import sys
import time
import tracemalloc
from concurrent.futures import ThreadPoolExecutor, as_completed
from pathlib import Path

sys.path.insert(0, os.path.join(os.path.dirname(__file__), ".."))

from environment.workplace_environment import WorkplaceEnvironment
from models import WorkplaceAction

BASELINE_PATH = Path(__file__).parent / "baseline.json"

ACTIONS = [
    WorkplaceAction(action_type="classify", content="refund"),
    WorkplaceAction(
        action_type="reply",
        content="Thank you for reaching out. We sincerely apologize for this experience. "
                "Your refund has been initiated and will be processed within 3-5 business days.",
    ),
    WorkplaceAction(action_type="escalate", content="no"),
]


def run_episode(env: WorkplaceEnvironment) -> float:
    t0 = time.perf_counter()
    env.reset()
    for a in ACTIONS:
        env.step(a)
    return (time.perf_counter() - t0) * 1000


def measure() -> dict:
    print("🔬 Measuring performance baseline...")

    env = WorkplaceEnvironment()
    # Warmup
    for _ in range(10):
        run_episode(env)

    # 1. Latency
    print("  ⏱  Latency (500 episodes)...")
    latencies = [run_episode(env) for _ in range(500)]
    latencies.sort()
    p50 = latencies[len(latencies) // 2]
    p95 = latencies[int(len(latencies) * 0.95)]
    p99 = latencies[int(len(latencies) * 0.99)]
    mean = statistics.mean(latencies)
    stddev = statistics.stdev(latencies)

    # 2. Sequential throughput
    print("  🚀 Sequential throughput (1000 episodes)...")
    t0 = time.perf_counter()
    for _ in range(1000):
        run_episode(env)
    seq_elapsed = time.perf_counter() - t0
    seq_throughput = 1000 / seq_elapsed

    # 3. Concurrent throughput
    print("  🔀 Concurrent throughput (4 threads × 250 episodes)...")
    def worker():
        e = WorkplaceEnvironment()
        for _ in range(250):
            run_episode(e)

    t0 = time.perf_counter()
    with ThreadPoolExecutor(max_workers=4) as pool:
        futures = [pool.submit(worker) for _ in range(4)]
        for f in as_completed(futures):
            f.result()
    conc_elapsed = time.perf_counter() - t0
    conc_throughput = 1000 / conc_elapsed

    # 4. Memory
    print("  🧠 Memory (100 episodes)...")
    gc.collect()
    tracemalloc.start()
    e = WorkplaceEnvironment()
    for _ in range(100):
        run_episode(e)
    _, peak = tracemalloc.get_traced_memory()
    tracemalloc.stop()
    peak_mb = peak / 1024 / 1024

    # Get git info
    try:
        commit = subprocess.check_output(
            ["git", "rev-parse", "--short", "HEAD"], text=True
        ).strip()
    except Exception:
        commit = "unknown"

    from datetime import datetime, timezone

    result = {
        "_meta": {
            "generated_at": datetime.now(timezone.utc).isoformat(),
            "commit": commit,
            "python": f"{sys.version_info.major}.{sys.version_info.minor}",
            "note": "Auto-generated by benchmarks/update_baseline.py",
        },
        "p50_ms": round(p50, 3),
        "p95_ms": round(p95, 3),
        "p99_ms": round(p99, 3),
        "mean_ms": round(mean, 3),
        "stddev_ms": round(stddev, 3),
        "throughput_sequential": round(seq_throughput, 1),
        "throughput_concurrent": round(conc_throughput, 1),
        "peak_memory_mb": round(peak_mb, 3),
    }

    return result


def main():
    parser = argparse.ArgumentParser(description="Update performance baseline")
    parser.add_argument("--confirm", action="store_true", help="Skip confirmation prompt (CI mode)")
    parser.add_argument("--dry-run", action="store_true", help="Measure but don't save")
    args = parser.parse_args()

    result = measure()

    print(f"\n{'=' * 60}")
    print("  NEW BASELINE VALUES")
    print(f"{'=' * 60}")
    for k, v in result.items():
        if k == "_meta":
            continue
        print(f"  {k:30s}  {v}")
    print(f"{'=' * 60}")

    # Compare with existing
    if BASELINE_PATH.exists():
        old = json.loads(BASELINE_PATH.read_text())
        print("\n  Changes from previous baseline:")
        for k in ["p50_ms", "p99_ms", "throughput_sequential", "peak_memory_mb"]:
            if k in old and k in result:
                old_v = old[k]
                new_v = result[k]
                delta = ((new_v - old_v) / max(old_v, 1e-9)) * 100
                icon = "⬆️" if delta > 0 else "⬇️"
                print(f"    {k}: {old_v} → {new_v} ({icon} {delta:+.1f}%)")

    if args.dry_run:
        print("\n[DRY RUN] Baseline not saved.")
        return

    if not args.confirm:
        resp = input("\nSave as new baseline? (yes/no): ")
        if resp.strip().lower() != "yes":
            print("Aborted.")
            return

    BASELINE_PATH.write_text(json.dumps(result, indent=2) + "\n")
    print(f"\n✅ Baseline saved to {BASELINE_PATH}")


if __name__ == "__main__":
    main()
